#include <stdio.h>

char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 97 && str[i] <= 122)
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}

int main()
{
	char	phrase[] = "bonjour bitch";

	printf("%s", ft_strupcase(phrase));
	return (0);
}
